    private void deletebuttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deletebuttonMouseClicked
        // TODO add your handling code here:
        if(drugidtxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter the Pharmacist to be Deleted");
        }else{
            try{
                 Con = DriverManager.getConnection("jdbc:mysql://localhost/pharmacydb", "root", "");
                 String id = drugidtxt.getText();
                 String query = "Delete from drug where DRUGID="+id;
                 Statement del = Con.createStatement();
                 del.executeUpdate(query);
                 SelectDrug();
                 JOptionPane.showMessageDialog(this, "Drug deleted Successfully");
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_deletebuttonMouseClicked

