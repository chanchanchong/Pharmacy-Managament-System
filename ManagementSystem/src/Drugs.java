    private void addbuttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addbuttonMouseClicked
        // TODO add your handling code here:
        if(drugidtxt.getText().isEmpty() || drugnametxt.getText().isEmpty() || qtytxt.getText().isEmpty()|| pricetxt.getText().isEmpty()){
           JOptionPane.showMessageDialog(this, "Missing Information");
       }else{
        try{
           // Class.forName("com.mysql.cj.jdbc.Driver");
            Con = DriverManager.getConnection("jdbc:mysql://localhost/pharmacydb", "root", "");
            PreparedStatement add= Con.prepareStatement("insert into drug values(?,?,?,?,?)");
            add.setInt(1, Integer.valueOf(drugidtxt.getText()));
            add.setString(2, drugnametxt.getText());
            add.setString(3, categorydb.getSelectedItem().toString());
            add.setString(4, qtytxt.getText());
            add.setString(5, pricetxt.getText());
            int row = add.executeUpdate();

            drugidtxt.setText("");
            drugnametxt.setText("");
            qtytxt.setText("");
            pricetxt.setText("");
            categorydb.setSelectedItem(0);
            Con.close();
            SelectDrug();
            JOptionPane.showMessageDialog(this, "Seller Added Successfully");
        }catch(Exception e){
            e.printStackTrace();
        }
       }
    }//GEN-LAST:event_addbuttonMouseClicked

